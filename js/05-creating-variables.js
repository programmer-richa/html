/************************************
 * Creating Variables and Constants
 * 
 * Variables using var or let keyword
 * 
 * Constants using const keyword
*/

// Variable declaration
var x = 10;
console.log(x);

let y = 20;
console.log(y);

y=30;
console.log(y);

// constant declaration

const z = 40;
console.log(z);

// z =50; This statement will generate error as we cannot modify the value of a constant. 
